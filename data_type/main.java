public class main {

    public static void main(String[] args) {

        long type_long = 0L;
        int type_int = 0;
        short type_short = 0;
        byte type_byte = 0;
        char type_char = '\u0000';
        boolean type_boolean = false;
        float type_float = 0.0F;
        double type_double = 0.0D;

        System.out.println("long = " + type_long);
        System.out.println("int = " + type_int);
        System.out.println("short = " + type_short);
        System.out.println("byte = " + type_byte);
        System.out.println("char = " + type_char);
        System.out.println("boolean = " + type_boolean);
        System.out.println("float = " + type_float);
        System.out.println("double = " + type_double);
    }
}