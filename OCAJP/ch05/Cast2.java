public class Cast2{
    public static void main(String[] args) {
        int x = 3 * 4;
        System.out.println(x);

        int a = 55555 * 66666;
        System.out.println(a);

        long b = 55555 * 66666;
        System.out.println(b);

        long c = (long)(55555 * 66666);
        System.out.println(c);

        long d = ((long)(55555)) * 66666;
        System.out.println(d);
    }
}